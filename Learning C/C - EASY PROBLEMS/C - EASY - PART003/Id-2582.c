#include <stdio.h>
#include <stdlib.h>

int main()
{
    int i = 0, count = 0, o[99], i, x, y, t = 0;
    char c = ' ';
    while (c == ' ')
    {
        scanf("%d%c", &o[i], &c);
        i++;
        count++;
    }
    scanf("%d\n%d", &x, &y);
    for (i = 0; i < count; i++)
    {
        if (o[i] % 2 == 0)
        {
            t -= y;
        }
        else
        {
            t += x;
        }
    }
    printf("%d", t);
}
